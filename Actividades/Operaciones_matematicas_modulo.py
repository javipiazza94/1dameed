def suma(a,b):
    """
    Returns:
        La suma de a y b
    """
    return a+b
    
def resta(a,b):
    """
    Returns:
        La resta de a y b
    """
    return a-b
    
def multiplicacion(a,b):
    """
    Returns:
        La multiplicacion de a y b
    """
    return a*b
    
def division(a,b):
    """
    Returns:
        La division de a y b
    """
    return a/b